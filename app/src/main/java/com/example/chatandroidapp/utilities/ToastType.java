package com.example.chatandroidapp.utilities;

/**
 * Enum representing different types of Toast messages.
 * Each type corresponds to a specific style and icon.
 *
 * @author Daniel Tongu
 */
public enum ToastType {
    /** Represents an informational toast. */
    INFO,

    /** Represents a warning toast.*/
    WARNING,

    /** Represents an error toast. */
    ERROR,

    /** Represents a success toast.*/
    SUCCESS,

    /** Represents a default toast with standard styling. */
    DEFAULT
}
